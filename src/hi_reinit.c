#include    <hi_std.h>

/**********************************************************************/
/*  再初期設定 - hi_reinit                                            */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   カーサスを使用する為の再初期設定を行う。                         */
/**********************************************************************/
vd  hi_reinit( struct ifa_s *ifa )      /* 再初期設定関数             */
{

    trace_start( ifa, "hi_reinit" ) ;   /* トレース取得               */
/* ------------------------------------------------------------------ */
/* ウィンドウの初期化                                                 */
/* ------------------------------------------------------------------ */
    ifa->win = initscr() ;              /* 編集ウィンドウの初期化     */
    cbreak();                           /* cbreakモードのセット       */
    noecho();                           /* エコ入力ーモード解除       */
    nonl();                             /* 復帰改行マップの解除       */
    crmode() ;                          /* 入力モード制御             */
    intrflush( ifa->win, FALSE );       /* 割り込みキーを制限         */
    keypad( ifa->win, TRUE ) ;          /* キーマップの割り当て       */
    ifa->curses = INIT ;                /* カーサス初期化済み         */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - hi_reinit         */
}                                       /* end (hi_reinit)            */


/**********************************************************************/
/*  画面再描画関数 - win_refresh                                      */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   乱れた画面の再描画を行う。                                       */
/**********************************************************************/
vd  win_refresh( struct ifa_s *ifa )    /* 画面再描画関数             */
{

    trace_start( ifa, "win_refresh" ) ; /* トレース取得               */
/* ------------------------------------------------------------------ */
/* ウィンドウのクリア                                                 */
/* ------------------------------------------------------------------ */
    clear() ;                           /* ウィンドウ全体のクリア     */

/* ------------------------------------------------------------------ */
/* 乱れた画面の再描画を行う                                           */
/* ------------------------------------------------------------------ */
    head_disp( ifa ) ;                  /* ヘッダ表示関数             */
    refresh() ;                         /* ウィンドウ再描画           */
    data_view( ifa ) ;                  /* データ表示関数             */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - win_refresh       */
}                                       /* end (win_refresh)          */


#include    <string.h>
#include    <hi_std.h>

/**********************************************************************/
/*  EUC表示関数 - lang_euc_euc                                        */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   文字データを、EUC表示する。                                      */
/**********************************************************************/
vd  lang_euc_euc( struct ifa_s *ifa )   /* EUC表示関数                */
{
    li           cnt ;                  /* ループカウンタ             */
    li           pos_x ;                /* X座標                      */
    li           pos_y ;                /* Y座標                      */
    li           offset ;               /* オフセット                 */
    uc           check[2] ;             /* チェックデータ             */
    uc           char_data[32] ;        /* 文字データ                 */
    ch           word_flg ;             /* 2バイト文字フラグ          */

    trace_start( ifa, "lang_euc_euc" ) ;/* トレース取得               */
/* ------------------------------------------------------------------ */
/* 初期設定                                                           */
/* ------------------------------------------------------------------ */
    pos_x = BASE_X3 ;                   /* ベース座標(X)の設定        */
    pos_y = BASE_Y ;                    /* ベース座標(Y)の設定        */
    offset = 0 ;                        /* オフセットの設定           */
    word_flg = 0 ;                      /* 2バイト文字フラグの初期化  */
    memset( char_data, 0x00, 32 ) ;     /* 文字データの初期化         */

/* ------------------------------------------------------------------ */
/* データチェック                                                     */
/* ------------------------------------------------------------------ */
    if (((uc)ifa->workarea[ifa->view_offset] >= 0xa1)&&
        ((uc)ifa->workarea[ifa->view_offset] <= 0xfe)) {
                                        /* 2バイト文字                */
        for ( cnt = ifa->view_offset ; cnt >= 0 ; cnt-- ) {
                                        /* 前データチェック           */
            if (((uc)ifa->workarea[cnt] < 0xa1)||
                ((uc)ifa->workarea[cnt] > 0xfe)) {
                                        /* 2バイト文字以外            */
                if ( ( ( cnt - ifa->view_offset ) % 2 ) == 0 ) {
                                        /* 表示データは2バイト文字    */
                    word_flg = 1 ;      /* 2バイト文字処理中          */
                }                       /* end if                     */
                break ;                 /* break for                  */
            }                           /* end if                     */
        }                               /* end for                    */
    }                                   /* end if                     */

/* ------------------------------------------------------------------ */
/* データの表示                                                       */
/* ------------------------------------------------------------------ */
    for ( cnt = ifa->view_offset ; cnt < ifa->size ; cnt++ ) {
                                        /* 全データの処理             */
        if ( pos_y == ( ifa->lines - 1 ) ) {
                                        /* 最終行                     */
            break ;                     /* データ表示の終了           */
        }                               /* end if                     */

/* ------------------------------------------------------------------ */
/* 文字コードをチェックする                                           */
/* ------------------------------------------------------------------ */
        char_data[16] = 0x20 ;          /* エリア外の1バイトにを設定  */
        memcpy(check, &ifa->workarea[cnt], 2) ;
                                        /* チェックデータ(2バイト)取得*/
        if ( word_flg == 0 ) {          /* 1バイト文字処理中          */
            if (((check[0] >= 0xa1)&&(check[0] <= 0xfe))&&
                ((check[1] >= 0xa1)&&(check[1] <= 0xfe))) {
                                        /* 可視文字(2バイト文字)      */
                word_flg = 1 ;          /* 2バイト文字の設定          */
                if ( offset == 15 ) {   /* 行の最後                   */
                    memcpy( &char_data[offset], check, 2 ) ;
                                        /* そのまま設定               */
                    offset++ ;          /* オフセットの更新           */
                }                       /* end if                     */
                else {                  /* 行の途中                   */
                    memcpy( &char_data[offset], check, 2 ) ;
                                        /* そのまま設定               */
                    offset += 2 ;       /* オフセットの更新           */
                }                       /* end else                   */
            }                           /* end if                     */
            else if ((check[0] >= 0x20)&&(check[0] <= 0x7e)) {
                                        /* 可視文字(1バイト文字)      */
                char_data[offset] = check[0] ;
                                        /* そのまま設定               */
                offset++ ;              /* オフセットの更新           */
            }                           /* end else if                */
            else {                      /* 不可視文字                 */
                char_data[offset] = TEN ;
                                        /* 不可視文字の為,(.)に変換   */
                offset++ ;              /* オフセットの更新           */
            }                           /* end else                   */
        }                               /* end if                     */
        else {                          /* 2バイト文字処理中          */
            word_flg = 0 ;              /* 2バイト文字の解除          */
            if ( offset == 0 ) {        /* 行の先頭                   */
                char_data[offset] = '@' ;
                                        /* 特殊文字の設定             */
                offset++ ;              /* オフセットの更新           */
            }                           /* end if                     */
        }                               /* end else                   */

        if ( ( cnt % 16 ) == 15 ) {     /* 1行の終了                  */
            move(pos_y, pos_x) ;        /* カーソルの移動             */
            printw("%.17s", char_data) ;/* 文字データの表示           */
            pos_y++ ;                   /* 次の行へ                   */
            offset = 0 ;                /* オフセットの初期化         */
        }                               /* end if                     */
    }                                   /* end for                    */

    refresh() ;                         /* ウィンドウの再描画         */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - lang_euc_euc      */
}                                       /* end (lang_euc_euc)         */


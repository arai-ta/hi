/**********************************************************************/
/*  データ挿入(先頭)関数(HEX) - head_h_input                          */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   ファイルの先頭位置から、データの挿入を行う。                     */
/**********************************************************************/
vd  head_h_input( struct ifa_s *ifa )   /* データ挿入(先頭)関数(HEX)  */
{

    trace_start( ifa, "head_h_input" ) ;/* トレース取得               */

    top_view( ifa ) ;                   /* カーソル移動関数(先頭)     */
    edit_h_input( ifa ) ;               /* データ挿入関数(HEX)        */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - head_h_input      */
}                                       /* end (head_h_input)         */


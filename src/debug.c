#include    <hi_std.h>

/**********************************************************************/
/*  デバッグ関数 - debug                                              */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   指定データをデバッグファイルに出力する。                         */
/**********************************************************************/
vd  debug( struct ifa_s *ifa )          /* デバッグ関数               */
{
    FILE  *fp ;                         /* ファイルポインタ           */

    fp = fopen( DEBUG_FILE, "ab+" ) ;   /* デバッグファイルオープン   */
    if ( fp != NULL ) {                 /* ファイルオープン成功       */
        if ( ifa->debug_msg[0] != 0x00 ) {
                                        /* 付加情報が設定されている   */
    	    fprintf(fp, "%s\n", ifa->debug_msg) ;
                                        /* 付加情報文の出力           */
        }                               /* end if                     */
        fclose( fp ) ;                  /* デバッグファイルクローズ   */
    }                                   /* end if                     */

    return ;                            /* return - debug             */
}                                       /* end (debug)                */


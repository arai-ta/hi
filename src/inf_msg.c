#include    <hi_std.h>

/**********************************************************************/
/*  インフォメーション表示関数 - inf_msg                              */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   指定されたインフォメーションを表示する。                         */
/**********************************************************************/
vd  inf_msg( struct ifa_s *ifa )        /* インフォメーション表示関数 */
{

    trace_start( ifa, "inf_msg" ) ;     /* トレース取得               */
/* ------------------------------------------------------------------ */
/* インフォメーション表示処理                                         */
/* ------------------------------------------------------------------ */
    echo() ;                            /* エコー入力モード           */
    move(ifa->inf_msg_y, ifa->inf_msg_x) ;
                                        /* インフォメーションエリアに */
                                        /* 移動                       */
    clrtobot() ;                        /* インフォメーションエリアの */
                                        /* クリア                     */
    addstr( ifa->inf_msg ) ;            /* インフォメーションの表示   */
    move(ifa->cur_pos_y, ifa->cur_pos_x) ;
                                        /* カーソル位置を戻す         */
    refresh() ;                         /* 画面の再描画               */
    noecho() ;                          /* エコー入力モード解除       */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - inf_msg           */
}                                       /* end (inf_msg)              */


#include    <hi_std.h>

/**********************************************************************/
/*  モード表示関数 - mode_dsp                                         */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   モード表示エリアに、現在のモードを表示する。                     */
/**********************************************************************/
vd  mode_dsp( struct ifa_s *ifa )       /* モード表示関数             */
{

    trace_start( ifa, "mode_dsp" ) ;    /* トレース取得               */
/* ------------------------------------------------------------------ */
/* モード表示処理                                                     */
/* ------------------------------------------------------------------ */
    mvaddstr(MODE_Y, MODE_X, ifa->mode_msg) ;
                                        /* モードの表示               */
    move(ifa->cur_pos_y, ifa->cur_pos_x) ;
                                        /* カーソル位置を戻す         */
    refresh() ;                         /* 画面の再描画               */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - mode_dsp          */
}                                       /* end (mode_dsp)             */


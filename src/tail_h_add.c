/**********************************************************************/
/*  データ追加(最終)関数(HEX) - tail_h_add                            */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   ファイルの最終位置から、データの追加を行う。                     */
/**********************************************************************/
vd  tail_h_add( struct ifa_s *ifa )     /* データ追加(最終)関数(HEX)  */
{

    trace_start( ifa, "tail_h_add" ) ;  /* トレース取得               */

    tail_view( ifa ) ;                  /* カーソル移動関数(最終)     */
    edit_h_add( ifa ) ;                 /* データ追加関数(HEX)        */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - tail_h_add        */
}                                       /* end (tail_h_add)           */


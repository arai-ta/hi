/**********************************************************************/
/*  データ追加(最終)関数(文字) - tail_c_add                           */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   ファイルの最終位置から、データの追加を行う。                     */
/**********************************************************************/
vd  tail_c_add( struct ifa_s *ifa )     /* データ追加(最終)関数(文字) */
{

    trace_start( ifa, "tail_c_add" ) ;  /* トレース取得               */

    tail_view( ifa ) ;                  /* カーソル移動関数(最終)     */
    edit_c_add( ifa ) ;                 /* データ追加関数(文字)       */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - tail_c_add        */
}                                       /* end (tail_c_add)           */


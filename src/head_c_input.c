/**********************************************************************/
/*  データ挿入(先頭)関数(文字) - head_c_input                         */
/* ------------------------------------------------------------------ */
/* 関数概要                                                           */
/*   ファイルの先頭位置から、データの挿入を行う。                     */
/**********************************************************************/
vd  head_c_input( struct ifa_s *ifa )   /* データ挿入(先頭)関数(文字) */
{

    trace_start( ifa, "head_c_input" ) ;/* トレース取得               */

    top_view( ifa ) ;                   /* カーソル移動関数(先頭)     */
    edit_c_input( ifa ) ;               /* データ挿入関数(文字)       */

    trace_end( ifa ) ;                  /* トレース取得               */
    return ;                            /* return - head_c_input      */
}                                       /* end (head_c_input)         */


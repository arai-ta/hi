/* インターフェースエリア定義                                         */
/* ================================================================== */
struct ifa_s {                /* インターフェースエリア               */
                              /* ==================================== */
    li      entry  ;          /* ファイルエントリ                     */
                              /* ------------------------------------ */
                              /*   対象ファイル内の通番を設定する     */
                              /* ==================================== */
    li      maxent ;          /* ファイル総数                         */
                              /* ------------------------------------ */
                              /*   対象ファイルの総数を設定する       */
                              /* ==================================== */
    ch      status ;          /* 状態フラグ                           */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | NON_SERVICE   | サービス中以外   | */
                              /* | SERVICE       | サービス中       | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      endflg ;          /* 終了フラグ                           */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | N_END         | 終了指示         | */
                              /* | D_END         | 強制終了指示     | */
                              /* | ABORT         | 強制終了指示     | */
                              /* | CONTINUE      | 処理継続         | */
                              /* | CHANGE        | 編集対象切り替え | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      envout ;          /* 動作環境表示フラグ                   */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | ENVOUT        | 動作環境表示     | */
                              /* | NOT_ENVOUT    | 動作環境未表示   | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      option ;          /* 操作対象                             */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | FIL           | ファイル         | */
                              /* | SHM           | 共有メモリ       | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      curses ;          /* カーサスフラグ                       */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | INIT          | 初期化済み       | */
                              /* | NOT_INIT      | 未初期化         | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      modif ;           /* 修正有無                             */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | MOD_ON        | 修正有り         | */
                              /* | MOD_OFF       | 修正無し         | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      target ;          /* 修正対象                             */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | HEX_DATA      | HEXデータエリア  | */
                              /* | CHAR_DATA     | 文字データエリア | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      exist ;           /* ファイル有無                         */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | EXIST         | 存在する         | */
                              /* | NOT_EXIST     | 存在しない       | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      lang ;            /* 言語                                 */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | LANG_ASCII    | ASCII            | */
                              /* | LANG_EUC      | Japanese EUC     | */
                              /* | LANG_SJIS     | Shift-JIS        | */
                              /* | LANG_JIS      | JIS              | */
                              /* | LANG_EBCDIC   | EBCDIC(K)        | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      ebcdic ;          /* EBCDIC(K)の種類                      */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | EBCDIC_IBM    | EBCDIC IBM       | */
                              /* | EBCDIK_HITAC  | EBCDIK HITACHI   | */
                              /* | EBCDIC_NEC    | EBCDIC NEC       | */
                              /* | EBCDIC        | EBCDIC           | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      term_lang ;       /* 端末の言語属性                       */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | LANG_ASCII    | C                | */
                              /* | LANG_EUC      | Japanese EUC     | */
                              /* | LANG_SJIS     | Shift-JIS        | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      dmy02[13] ;       /* 予備                                 */
                              /* ==================================== */
    ch      file[256] ;       /* 操作対象ファイル名                   */
                              /* ------------------------------------ */
                              /*   操作対象が(FIL)の時に、ファイル名  */
                              /*   を設定する                         */
                              /* ==================================== */
    ch      shmid[32] ;       /* 操作対象共有メモリID                 */
                              /* ------------------------------------ */
                              /*   操作対象が(SHM)の時に、共有メモリ  */
                              /*   IDを設定する                       */
                              /* ==================================== */
    li      size ;            /* データサイズ                         */
                              /* ------------------------------------ */
                              /*   編集中のデータサイズを保持する     */
                              /* ==================================== */
    li      area_size ;       /* 作業領域サイズ                       */
                              /* ------------------------------------ */
                              /*   編集中のエリア総サイズを保持する   */
                              /* ==================================== */
    li      columns ;         /* ウィンドウ:桁                        */
                              /* ------------------------------------ */
                              /*   ウィンドウの桁数を保持する         */
                              /* ==================================== */
    li      lines ;           /* ウィンドウ:行                        */
                              /* ------------------------------------ */
                              /*   ウィンドウの行数を保持する         */
                              /* ==================================== */
    li      view_offset ;     /* 表示オフセット                       */
                              /* ------------------------------------ */
                              /*   画面の左上アドレスを保持する       */
                              /* ==================================== */
    li      data_offset ;     /* データオフセット                     */
                              /* ------------------------------------ */
                              /*   カーソル位置のアドレスを保持する   */
                              /* ==================================== */
    li      cmd ;             /* コマンド                             */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | CMD_CUR_DOWN  | カーソル下移動   | */
                              /* | CMD_CUR_UP    | カーソル上移動   | */
                              /* | CMD_CUR_LEFT  | カーソル左移動   | */
                              /* | CMD_CUR_RIGHT | カーソル右移動   | */
                              /* | CMD_PREV_PAGE | ページ上移動     | */
                              /* | CMD_NEXT_PAGE | ページ下移動     | */
                              /* | CMD_PREV_HALF | 半ページ上移動   | */
                              /* | CMD_NEXT_HALF | 半ページ下移動   | */
                              /* | CMD_TAIL      | 最終に移動       | */
                              /* | CMD_LINE_END  | 行の最終に移動   | */
                              /* | CMD_LINE_START| 行の先頭に移動   | */
                              /* | CMD_CPOS_MOVE | カーソル位置切替 | */
                              /* | CMD_REPEAT    | 繰り返し         | */
                              /* | CMD_INPUT     | データ挿入       | */
                              /* | CMD_TOP_INPUT | データ挿入(先頭) | */
                              /* | CMD_ADD       | データ追加       | */
                              /* | CMD_LAST_ADD  | データ追加(最終) | */
                              /* | CMD_REPLACE_1 | データ変更(単一) | */
                              /* | CMD_REPLACE_2 | データ変更(複数) | */
                              /* | CMD_DELETE    | データ削除       | */
                              /* | CMD_REFRESH   | 画面リフレッシュ | */
                              /* | CMD_REPORT    | レポート表示     | */
                              /* | CMD_SEARCH    | サーチコマンド   | */
                              /* | CMD_NEXT      | 再検索           | */
                              /* | CMD_CNTRL     | 制御コマンド     | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    li      before_cmd ;      /* 前回のコマンド                       */
                              /* ------------------------------------ */
                              /*   前回のコマンドを保持する           */
                              /* ==================================== */
    ch      mode ;            /* モード                               */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | MODE_CMD      | コマンドモード   | */
                              /* | MODE_EDT      | 編集モード       | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      hex_pos ;         /* カーソル位置                         */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | CUR_LEFT      | 上位バイト       | */
                              /* | CUR_RIGHT     | 上位バイト       | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      image ;           /* 画面イメージ出力種別                 */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | CREATE        | 新規             | */
                              /* | ADD           | 追加             | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      reverse ;         /* 検索文字列反転種別                   */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | REVERSE       | 反転表示する     | */
                              /* | NOT_REVERSE   | 反転表示しない   | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      dmy03[12] ;       /* 予備                                 */
                              /* ==================================== */
    li      cur_pos_x ;       /* カーソル位置(X座標)                  */
                              /* ------------------------------------ */
                              /*   画面上のカーソル位置(X座標)を保持  */
                              /*   する                               */
                              /* ==================================== */
    li      cur_pos_y ;       /* カーソル位置(Y座標)                  */
                              /* ------------------------------------ */
                              /*   画面上のカーソル位置(Y座標)を保持  */
                              /*   する                               */
                              /* ==================================== */
    li      cur_cmd_x ;       /* コマンド位置(X座標)                  */
                              /* ------------------------------------ */
                              /*   コマンド入力エリア位置(X座標)を    */
                              /*   保持する                           */
                              /* ==================================== */
    li      cur_cmd_y ;       /* コマンド位置(Y座標)                  */
                              /* ------------------------------------ */
                              /*   コマンド入力エリア位置(X座標)を    */
                              /*   保持する                           */
                              /* ==================================== */
    li      inf_msg_x ;       /* インフォメーション表示位置(X座標)    */
                              /* ------------------------------------ */
                              /*   インフォメーション表示位置(X座標)  */
                              /*   を保持する                         */
                              /* ==================================== */
    li      inf_msg_y ;       /* インフォメーション表示位置(Y座標)    */
                              /* ------------------------------------ */
                              /*   インフォメーション表示位置(Y座標)  */
                              /*   を保持する                         */
                              /* ==================================== */
    li      err_msg_x ;       /* エラーメッセージ表示位置(X座標)      */
                              /* ------------------------------------ */
                              /*   エラーメッセージ表示位置(X座標)を  */
                              /*   保持する                           */
                              /* ==================================== */
    li      err_msg_y ;       /* エラーメッセージ表示位置(Y座標)      */
                              /* ------------------------------------ */
                              /*   エラーメッセージ表示位置(Y座標)を  */
                              /*   保持する                           */
                              /* ==================================== */
    li      alloc_size ;      /* 追加作業領域サイズ                   */
                              /* ------------------------------------ */
                              /*   ファイルを読み込む追加領域サイズを */
                              /*   保持する                           */
                              /* ==================================== */
    li      max_size ;        /* 作業領域                             */
                              /* ------------------------------------ */
                              /*   ファイルを読み込む最大領域サイズを */
                              /*   保持する                           */
                              /* ==================================== */
    ch     *workarea ;        /* 作業領域                             */
                              /* ------------------------------------ */
                              /*   ファイルを読み込む領域アドレス／   */
                              /*   共有メモリアドレスを保持する       */
                              /* ==================================== */
    ch      dmy04[4] ;        /* 予備                                 */
                              /* ==================================== */
    ch      cmd_str[64] ;     /* 制御コマンド                         */
                              /* ------------------------------------ */
                              /*   コマンド入力エリアに入力された制御 */
                              /*   コマンドを保持する                 */
                              /* ==================================== */
    li      cmd_size ;        /* 制御コマンドサイズ                   */
                              /* ------------------------------------ */
                              /*   コマンド入力エリアに入力された制御 */
                              /*   コマンドのサイズを保持する         */
                              /* ==================================== */
    ch      dmy05[12] ;       /* 予備                                 */
                              /* ==================================== */
    ch      search_str[64] ;  /* サーチコマンド                       */
                              /* ------------------------------------ */
                              /*   コマンド入力エリアに入力された     */
                              /*   サーチコマンドを保持する           */
                              /* ==================================== */
    li      search_size ;     /* サーチコマンドサイズ                 */
                              /* ------------------------------------ */
                              /*   コマンド入力エリアに入力された制御 */
                              /*   サーチコマンドサイズを保持する     */
                              /* ==================================== */
    ch      search ;          /* サーチ方向                           */
                              /* ----------------+------------------+ */
                              /* | 値            | 意味             | */
                              /* +---------------+------------------+ */
                              /* | NEXT_SEARCH   | 順検索           | */
                              /* | PREV_SEARCH   | 逆検索           | */
                              /* +---------------+------------------+ */
                              /* ==================================== */
    ch      dmy06[11] ;       /* 予備                                 */
                              /* ==================================== */
    ch      mode_msg[8] ;     /* モードメッセージ                     */
                              /* ------------------------------------ */
                              /*   モード表示エリアに表示する文字列を */
                              /*   保持する                           */
                              /* ==================================== */
    ch      dmy07[8] ;        /* 予備                                 */
                              /* ==================================== */
    ch      inf_msg[128] ;    /* インフォメーション                   */
                              /* ------------------------------------ */
                              /*   インフォメーション表示エリアに表示 */
                              /*   する文字列を保持する               */
                              /* ==================================== */
    ch      err_msg[64] ;     /* エラーメッセージ                     */
                              /* ------------------------------------ */
                              /*   エラーメッセージ表示エリアに表示   */
                              /*   する文字列を保持する               */
                              /* ==================================== */
    ch      perr_msg[64] ;    /* 障害(付加)情報                       */
                              /* ------------------------------------ */
                              /*   perror()に引き渡す情報を保持する   */
                              /* ==================================== */
    ch      debug_msg[64] ;   /* デバッグメッセージ                   */
                              /* ------------------------------------ */
                              /*   デバッグファイルに出力する付加情報 */
                              /*   を設定する                         */
                              /* ==================================== */
    li      help_line ;       /* ヘルプの行数                         */
                              /* ------------------------------------ */
                              /*   ヘルプファイルの行数を設定する     */
                              /* ==================================== */
    ch     *help_area ;       /* ヘルプ情報アドレス                   */
                              /* ------------------------------------ */
                              /*   ヘルプ情報を保持する領域のアドレス */
                              /*   を設定する                         */
                              /* ==================================== */
    ch     *search_info ;     /* 検索文字列情報アドレス               */
                              /* ------------------------------------ */
                              /*   検索文字列情報を保持する領域の     */
                              /*   アドレスを設定する                 */
                              /* ==================================== */
    li      func_dep ;        /* 関数トレースの深さ                   */
                              /* ------------------------------------ */
                              /*   関数トレースの深さを設定する       */
                              /* ==================================== */
    ch     *func_trace[16] ;  /* 関数トレース情報                     */
                              /* ------------------------------------ */
                              /*   関数トレース情報を格納するエリア   */
                              /* ==================================== */
    WINDOW  *win ;            /* WINDOW構造体                         */
                              /* ------------------------------------ */
                              /*   メインスクリーン(=stdscr)の情報    */
                              /* ==================================== */
    ch      dmy08[20] ;       /* 予備                                 */
                              /* ==================================== */
    ch      err_func[32] ;    /* 障害発生関数                         */
                              /* ------------------------------------ */
                              /*   障害が発生した関数の名称           */
                              /* ==================================== */
    ch      err_line[8] ;     /* 障害発生行数                         */
                              /* ------------------------------------ */
                              /*   障害が発生した行数                 */
                              /* ==================================== */
    ch      dmy09[24] ;       /* 予備                                 */
                              /* ==================================== */
} ;                           /* end - ifa_s                          */

